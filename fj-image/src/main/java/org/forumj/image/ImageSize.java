package org.forumj.image;

/**
 * Created by Andrew on 03/03/2017.
 */
public class ImageSize {
    int height;
    int width;

    public ImageSize(int height, int width) {
        super();
        this.height = height;
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
